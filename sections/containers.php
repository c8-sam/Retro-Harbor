<div class="container" id="containers">
    <h2>Containers</h2>
    <p>This framework comes with 2 differents ways of containing your content. The following is a grid based container system:</p>

    <ul class="no-style">
        <li><pre class="single">.grid-container <span class="comment">/* Grid Container */</span></pre></li>
        <li><pre class="single">.grid-full_width <span class="comment">/* Full Width (No Gutters) */</span></pre></li>
        <li><pre class="single">.grid-breakout <span class="comment">/* Max Width Content (No Gutters) */</span></pre></li>
        <li><pre class="single">.grid-content <span class="comment">/* Standard Content Area (With Gutters) */</span></pre></li>
        <li><pre class="single">.grid-indent <span class="comment">/* Indented Content (Extra Gutter Spacing)*/</span></pre></li>
        <li><pre class="single">.grid-thin <span class="comment">/* Thin Content (Extra Gutter Spacing)*/</span></pre></li>
        <li><pre class="single">.grid-extrathin <span class="comment">/* Extra Thin Content (Extra Gutter Spacing)*/</span></pre></li>
    </ul>

    <p>Example: <a href="/retro-harbor/examples/example-containers.php">Containers</a>

    <p>The following is a more traditional container system:</p>

    <ul class="no-style">
        <li><pre class="single">.container <span class="comment">/* Global Use */</span></pre></li>
        <li><pre class="single">.container__full-width <span class="comment">/* Full Width  (Without Gutters) */</span></pre></li>
        <li><pre class="single">.container__max-width <span class="comment">/* Max Width Container (With Gutters) */</span></pre></li>
        <li><pre class="single">.container__max-indent <span class="comment">/* Indented Container (With Gutters) */</span></pre></li>
        <li><pre class="single">.container__max-thin <span class="comment">/* Thin Container (With Gutters) */</span></pre></li>
        <li><pre class="single">.container__max-extrathin <span class="comment">/* Extra Thin Container (With Gutters) */</span></pre></li>
    </ul>

    <p>Example: <a href="/retro-harbor/examples/example-containers-shopify.php">Containers</a>

